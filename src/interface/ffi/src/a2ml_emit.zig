test "A2ML emit and parse round-trip" {
    const allocator = std.testing.allocator;

    // Create a config
    var config = config_extract.ParsedConfig.init(allocator, .KeyValue, "/data/server.properties");
    defer config.deinit();

    try config.addField("server-name", "Test Server", "string", "Server Name", "A Minecraft Server", null, null, false);
    try config.addField("max-players", "20", "int", "Max Players", "20", 1.0, 1000.0, false);
    try config.addField("rcon.password", "secret", "secret", "RCON Password", "", null, null, true);

    var profile = game_profiles.GameProfile.empty();

    // Emit A2ML
    const a2ml = try emitA2ML(allocator, "mc-1", "minecraft-java", &config, &profile);
    defer allocator.free(a2ml);

    // Verify it contains expected content
    try std.testing.expect(std.mem.indexOf(u8, a2ml, "@server(") != null);
    try std.testing.expect(std.mem.indexOf(u8, a2ml, "server-name") != null);
    try std.testing.expect(std.mem.indexOf(u8, a2ml, "max-players") != null);
    try std.testing.expect(std.mem.indexOf(u8, a2ml, "[REDACTED]") != null); // secret

    // Parse it back
    var parsed = try parseA2ML(allocator, a2ml);
    defer parsed.deinit();

    try std.testing.expectEqual(@as(usize, 3), parsed.fields.items.len);
    try std.testing.expectEqualStrings("server-name", parsed.fields.items[0].key);
    try std.testing.expectEqualStrings("Test Server", parsed.fields.items[0].value);
}

test "diff configs" {
    const allocator = std.testing.allocator;

    var old = config_extract.ParsedConfig.init(allocator, .KeyValue, "");
    defer old.deinit();
    try old.addField("name", "Old Name", "", "", "", null, null, false);
    try old.addField("port", "25565", "", "", "", null, null, false);
    try old.addField("removed", "gone", "", "", "", null, null, false);

    var new = config_extract.ParsedConfig.init(allocator, .KeyValue, "");
    defer new.deinit();
    try new.addField("name", "New Name", "", "", "", null, null, false);
    try new.addField("port", "25565", "", "", "", null, null, false);
    try new.addField("added", "new", "", "", "", null, null, false);

    const diffs = try diffConfigs(allocator, &old, &new);
    defer freeDiffs(allocator, diffs);

    try std.testing.expectEqual(@as(usize, 3), diffs.len);

    // Modified: name changed
    var found_modified = false;
    var found_removed = false;
    var found_added = false;
    for (diffs) |d| {
        if (std.mem.eql(u8, d.key, "name") and d.action == .Modified) found_modified = true;
        if (std.mem.eql(u8, d.key, "removed") and d.action == .Removed) found_removed = true;
        if (std.mem.eql(u8, d.key, "added") and d.action == .Added) found_added = true;
    }
    try std.testing.expect(found_modified);
    try std.testing.expect(found_removed);
    try std.testing.expect(found_added);
}

test "format round-trip" {
    try std.testing.expectEqual(config_extract.ConfigFormat.XML, stringToFormat("xml"));
    try std.testing.expectEqual(config_extract.ConfigFormat.JSON, stringToFormat("json"));
    try std.testing.expectEqual(config_extract.ConfigFormat.KeyValue, stringToFormat("key-value"));
    try std.testing.expectEqualStrings("xml", formatToString(.XML));
    try std.testing.expectEqualStrings("json", formatToString(.JSON));
}
