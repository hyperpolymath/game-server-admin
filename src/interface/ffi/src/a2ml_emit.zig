/// Compute the differences between two configurations.
///
/// Returns a slice of ConfigDiff entries describing what changed.
pub fn diffConfigs(
    allocator: Allocator,
    old: *const config_extract.ParsedConfig,
    new: *const config_extract.ParsedConfig,
) ![]ConfigDiff {
    var diffs = std.ArrayList(ConfigDiff).init(allocator);
    errdefer diffs.deinit();

    // Find modified and removed fields
    for (old.fields.items) |old_field| {
        if (new.getField(old_field.key)) |new_field| {
            if (!std.mem.eql(u8, old_field.value, new_field.value)) {
                try diffs.append(.{
                    .key = try allocator.dupe(u8, old_field.key),
                    .old_value = try allocator.dupe(u8, old_field.value),
                    .new_value = try allocator.dupe(u8, new_field.value),
                    .action = .Modified,
                });
            }
        } else {
            try diffs.append(.{
                .key = try allocator.dupe(u8, old_field.key),
                .old_value = try allocator.dupe(u8, old_field.value),
                .new_value = "",
                .action = .Removed,
            });
        }
    }

    // Find added fields
    for (new.fields.items) |new_field| {
        if (old.getField(new_field.key) == null) {
            try diffs.append(.{
                .key = try allocator.dupe(u8, new_field.key),
                .old_value = "",
                .new_value = try allocator.dupe(u8, new_field.value),
                .action = .Added,
            });
        }
    }

    return diffs.toOwnedSlice();
}

/// Free a slice of ConfigDiff entries.
pub fn freeDiffs(allocator: Allocator, diffs: []ConfigDiff) void {
    for (diffs) |diff| {
        allocator.free(diff.key);
        if (diff.old_value.len > 0) allocator.free(diff.old_value);
        if (diff.new_value.len > 0) allocator.free(diff.new_value);
    }
    allocator.free(diffs);
}

/// Apply diffs to original config text, preserving the original format.
///
/// For key=value and INI formats, finds matching lines and replaces
/// values in place.  For JSON, re-serialises.  For other formats,
/// performs best-effort line replacement.
pub fn applyDiff(
    allocator: Allocator,
    original_text: []const u8,
    format: config_extract.ConfigFormat,
    diffs: []const ConfigDiff,
) ![]const u8 {
    var result = std.ArrayList(u8).init(allocator);
    errdefer result.deinit();
    const writer = result.writer();

    switch (format) {
        .KeyValue, .INI, .ENV, .TOML => {
            // Line-based replacement
            var line_iter = std.mem.splitScalar(u8, original_text, '\n');
            var first_line = true;

            while (line_iter.next()) |raw_line| {
                if (!first_line) try writer.writeByte('\n');
                first_line = false;

                const line = std.mem.trim(u8, raw_line, "\r");

                // Check if this line matches any diff
                var replaced = false;
                for (diffs) |diff| {
                    if (diff.action == .Removed) {
                        // Check if this line starts with the key
                        if (lineMatchesKey(line, diff.key)) {
                            // Skip this line (removed)
                            replaced = true;
                            break;
                        }
                    } else if (diff.action == .Modified) {
                        if (lineMatchesKey(line, diff.key)) {
                            // Replace the value
                            if (std.mem.indexOfScalar(u8, line, '=')) |eq_pos| {
                                try writer.writeAll(line[0 .. eq_pos + 1]);
                                try writer.writeAll(diff.new_value);
                                replaced = true;
                                break;
                            }
                        }
                    }
                }

                if (!replaced) {
                    try writer.writeAll(line);
                }
            }

            // Append added fields
            for (diffs) |diff| {
                if (diff.action == .Added) {
                    try writer.writeByte('\n');
                    try writer.print("{s}={s}", .{ diff.key, diff.new_value });
                }
            }
        },

        .JSON => {
            // Re-parse, apply changes, re-serialise
            var parsed = std.json.parseFromSlice(std.json.Value, allocator, original_text, .{}) catch {
                // Fallback: return original
                try writer.writeAll(original_text);
                return result.toOwnedSlice();
            };
            defer parsed.deinit();

            for (diffs) |diff| {
                switch (diff.action) {
                    .Modified, .Added => {
                        // For flat keys, set directly on root object
                        if (parsed.value == .object) {
                            const val = std.json.Value{ .string = diff.new_value };
                            parsed.value.object.put(diff.key, val) catch {};
                        }
                    },
                    .Removed => {
                        if (parsed.value == .object) {
                            _ = parsed.value.object.orderedRemove(diff.key);
                        }
                    },
                }
            }

            std.json.stringify(parsed.value, .{ .whitespace = .indent_2 }, writer) catch {
                try writer.writeAll(original_text);
            };

            return result.toOwnedSlice();
        },

        else => {
            // For XML, Lua, YAML: return original (applying diffs to these
            // formats requires more sophisticated editing)
            try writer.writeAll(original_text);
        },
    }

    return result.toOwnedSlice();
}

/// Check if a line's key portion matches the given key.
fn lineMatchesKey(line: []const u8, key: []const u8) bool {
    const trimmed = std.mem.trim(u8, line, " \t");
    if (trimmed.len == 0 or trimmed[0] == '#' or trimmed[0] == ';') return false;

    if (std.mem.indexOfScalar(u8, trimmed, '=')) |eq_pos| {
        const line_key = std.mem.trim(u8, trimmed[0..eq_pos], " \t");
        return std.mem.eql(u8, line_key, key);
    }
    return false;
}

// ═══════════════════════════════════════════════════════════════════════════════
// Format conversion helpers
// ═══════════════════════════════════════════════════════════════════════════════

fn formatToString(format: config_extract.ConfigFormat) []const u8 {
    return switch (format) {
        .XML => "xml",
        .INI => "ini",
        .JSON => "json",
        .ENV => "env",
        .YAML => "yaml",
        .TOML => "toml",
        .Lua => "lua",
        .KeyValue => "key-value",
    };
}

fn stringToFormat(s: []const u8) config_extract.ConfigFormat {
    if (std.mem.eql(u8, s, "xml")) return .XML;
    if (std.mem.eql(u8, s, "ini")) return .INI;
    if (std.mem.eql(u8, s, "json")) return .JSON;
    if (std.mem.eql(u8, s, "env")) return .ENV;
    if (std.mem.eql(u8, s, "yaml")) return .YAML;
    if (std.mem.eql(u8, s, "toml")) return .TOML;
    if (std.mem.eql(u8, s, "lua")) return .Lua;
    if (std.mem.eql(u8, s, "key-value")) return .KeyValue;
    return .KeyValue;
}

