// Game Server Admin — A2ML serialisation and deserialisation
//
// Converts between ParsedConfig structures and A2ML (Annotated Attribute
// Markup Language) text.  A2ML is the canonical interchange format used
// by hyperpolymath projects for structured configuration.
//
// Output format:
//   @server(id="...", game="...", host="..."):
//     @probe(protocol="...", fingerprint="..."):@end
//     @config(format="...", path="..."):
//       @field(key="...", type="...", label="...", default="..."):
//         "Current Value"
//       @end
//     @end
//   @end

const std = @import("std");
const Allocator = std.mem.Allocator;
const config_extract = @import("config_extract.zig");
const game_profiles = @import("game_profiles.zig");
const main = @import("main.zig");

// ═══════════════════════════════════════════════════════════════════════════════
// Types
