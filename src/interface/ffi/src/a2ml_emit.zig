/// A single difference between two config versions.
pub const ConfigDiff = struct {
    key: []const u8,
    old_value: []const u8,
    new_value: []const u8,
    action: DiffAction,
};

/// The kind of change represented by a ConfigDiff.
pub const DiffAction = enum(u8) {
    Added = 0,
    Modified = 1,
    Removed = 2,
};

// ═══════════════════════════════════════════════════════════════════════════════
// A2ML emission
// ═══════════════════════════════════════════════════════════════════════════════

/// Emit a full A2ML document for a server configuration.
///
/// The output contains the server metadata block, an optional probe
/// block, and all config fields annotated with type/label/default
/// information from the game profile.
pub fn emitA2ML(
    allocator: Allocator,
    server_id: []const u8,
    game_id: []const u8,
    config: *const config_extract.ParsedConfig,
    profile: *const game_profiles.GameProfile,
) ![]const u8 {
    var buf = std.ArrayList(u8).init(allocator);
    errdefer buf.deinit();
    const writer = buf.writer();

    // SPDX header
    try writer.writeAll("# SPDX-License-Identifier: PMPL-1.0-or-later\n");
    try writer.writeAll("# Generated by Game Server Admin FFI layer\n\n");

    // Server block
    try writer.print("@server(id=\"{s}\", game=\"{s}\"):\n", .{ server_id, game_id });

    // Probe block (protocol and fingerprint from profile)
    try writer.print("  @probe(protocol=\"{s}\"):", .{profile.protocol});
    try writer.writeAll("@end\n");

    // Config block
    const format_name = formatToString(config.format);
    try writer.print("  @config(format=\"{s}\", path=\"{s}\"):\n", .{ format_name, config.path });

    // Emit each field
    for (config.fields.items) |field| {
        try emitField(&field, writer, profile);
    }

    try writer.writeAll("  @end\n");
    try writer.writeAll("@end\n");

    return buf.toOwnedSlice();
}

/// Emit a single config field as an A2ML @field block.
///
/// If a matching FieldDef exists in the profile, enriches the output
/// with type, label, default, and constraint information.
pub fn emitField(
    field: *const config_extract.ConfigField,
    writer: anytype,
    profile: *const game_profiles.GameProfile,
) !void {
    // Look up profile definition for this key
    const def = profile.getFieldDef(field.key);

    const field_type = if (def) |d| d.field_type else field.field_type;
    const label = if (def) |d| d.label else field.label;
    const default_val = if (def) |d| d.default_val else field.default_val;

    try writer.print("    @field(key=\"{s}\"", .{field.key});

    if (field_type.len > 0) {
        try writer.print(", type=\"{s}\"", .{field_type});
    }
    if (label.len > 0) {
        try writer.print(", label=\"{s}\"", .{label});
    }
    if (default_val.len > 0) {
        try writer.print(", default=\"{s}\"", .{default_val});
    }
    if (field.is_secret) {
        try writer.writeAll(", secret=\"true\"");
    }

    // Range constraints
    if (def) |d| {
        if (d.range_min) |rmin| {
            try writer.print(", min=\"{d}\"", .{rmin});
        }
        if (d.range_max) |rmax| {
            try writer.print(", max=\"{d}\"", .{rmax});
        }
    }

    try writer.writeAll("):\n");

    // Emit current value (secrets are redacted)
    if (field.is_secret) {
        try writer.writeAll("      \"[REDACTED]\"\n");
    } else {
        try writer.print("      \"{s}\"\n", .{field.value});
    }

    try writer.writeAll("    @end\n");
}

// ═══════════════════════════════════════════════════════════════════════════════
// A2ML parsing
// ═══════════════════════════════════════════════════════════════════════════════

/// Parse an A2ML document back into a structured ParsedConfig.
///
/// This is the inverse of emitA2ML.  It extracts @field blocks and
/// their values, reconstructing the config fields.
pub fn parseA2ML(allocator: Allocator, a2ml: []const u8) !config_extract.ParsedConfig {
    var config = config_extract.ParsedConfig.init(allocator, .KeyValue, "");
    errdefer config.deinit();
    config.raw_text = try allocator.dupe(u8, a2ml);

    // Parse @config format attribute
    if (std.mem.indexOf(u8, a2ml, "@config(")) |config_start| {
        const after_config = a2ml[config_start..];
        if (extractA2MLAttr(after_config, "format")) |fmt_str| {
            config.format = stringToFormat(fmt_str);
        }
        if (extractA2MLAttr(after_config, "path")) |path| {
            config.path = path;
        }
    }

    // Parse @field blocks
    var pos: usize = 0;
    while (pos < a2ml.len) {
        const field_start = std.mem.indexOfPos(u8, a2ml, pos, "@field(") orelse break;
        const field_end = std.mem.indexOfPos(u8, a2ml, field_start, "@end") orelse break;

        const field_block = a2ml[field_start..field_end];
        const attrs_end = std.mem.indexOfScalar(u8, field_block, ')') orelse {
            pos = field_end + 4;
            continue;
        };

        const attrs = field_block[7..attrs_end]; // skip "@field("

        const key = extractA2MLAttr(attrs, "key") orelse {
            pos = field_end + 4;
            continue;
        };
        const field_type = extractA2MLAttr(attrs, "type") orelse "";
        const label = extractA2MLAttr(attrs, "label") orelse "";
        const default_val = extractA2MLAttr(attrs, "default") orelse "";
        const is_secret_str = extractA2MLAttr(attrs, "secret");
        const is_secret = if (is_secret_str) |s| std.mem.eql(u8, s, "true") else false;

        // Extract value (the quoted string on the next line)
        const value_area = field_block[attrs_end + 1 ..];
        const value = extractQuotedValue(value_area) orelse "";

        // Parse min/max
        var range_min: ?f64 = null;
        var range_max: ?f64 = null;
        if (extractA2MLAttr(attrs, "min")) |min_str| {
            range_min = std.fmt.parseFloat(f64, min_str) catch null;
        }
        if (extractA2MLAttr(attrs, "max")) |max_str| {
            range_max = std.fmt.parseFloat(f64, max_str) catch null;
        }

        try config.addField(key, value, field_type, label, default_val, range_min, range_max, is_secret);

        pos = field_end + 4;
    }

    return config;
}

/// Extract a named attribute value from an A2ML attribute string.
/// Looks for `name="value"` patterns.
fn extractA2MLAttr(attrs: []const u8, name: []const u8) ?[]const u8 {
    var search_buf: [128]u8 = undefined;
    const search = std.fmt.bufPrint(&search_buf, "{s}=\"", .{name}) catch return null;

    if (std.mem.indexOf(u8, attrs, search)) |start| {
        const val_start = start + search.len;
        if (std.mem.indexOfScalarPos(u8, attrs, val_start, '"')) |val_end| {
            return attrs[val_start..val_end];
        }
    }
    return null;
}

/// Extract a quoted value from a block of text.
/// Looks for the first `"..."` on any line.
fn extractQuotedValue(text: []const u8) ?[]const u8 {
    var line_iter = std.mem.splitScalar(u8, text, '\n');
    while (line_iter.next()) |raw_line| {
        const line = std.mem.trim(u8, raw_line, " \t\r");
        if (line.len < 2) continue;
        if (line[0] == '"') {
            if (std.mem.lastIndexOfScalar(u8, line, '"')) |last_q| {
                if (last_q > 0) return line[1..last_q];
            }
        }
    }
    return null;
}

